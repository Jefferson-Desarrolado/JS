let mes=String(prompt("Elije un numero de dia"))
switch(mes){
    case '1':
        alert("lunes");
        break;
    case '2':
        alert("martes");
        break;
    case '3':
        alert("miercoles");
        break;
    case '4':
        alert("jueves");
        break;
    case '5':
        alert("vierenes");
        break;
    case '6':
        alert("sabado");
        break;
    case '7':
        alert("domingo");
        break;
    default:
        alert("dia no existe")
        

}
